package com.day01_code.demo01;


public class EqualsTest02 {
    private String name;
    private int age;







    //无参构造
    public EqualsTest02() {

    }

    public EqualsTest02(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }


}
